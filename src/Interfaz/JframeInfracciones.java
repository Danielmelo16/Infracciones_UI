    private void BotonGuardarIngreseNuevaInfraccionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotonGuardarIngreseNuevaInfraccionMouseClicked
        // TODO add your handling code here:
        if (!jTextArea2.getText().isEmpty()) {
            try {
                PreparedStatement ps;
                ps = bD.conexion().prepareStatement("INSERT INTO infracciones VALUES (?, ?)");
                ps.setString(1, jTextField4.getText());
                ps.setString(2, jTextArea2.getText());
                ps.execute();
                JOptionPane.showMessageDialog(this, "Registro de infraccion exitoso", "Inserccion exitosa", JOptionPane.INFORMATION_MESSAGE, null);
                addTable();
            } catch (SQLException ex) {
                Logger.getLogger(JframeInfracciones.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor ingrese una descripcion de la descripcion", "Validacion", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BotonGuardarIngreseNuevaInfraccionMouseClicked

