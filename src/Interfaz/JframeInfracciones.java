    public void getCodInfraccion() {
        Statement s;
        try {
            s = (Statement) bD.conexion().createStatement();
            ResultSet rs = s.executeQuery("SELECT COD_INFRACCION FROM infracciones ORDER BY COD_INFRACCION DESC LIMIT 1");
            while (rs.next()) {
                jTextField4.setText(String.valueOf(rs.getInt("COD_INFRACCION") + 1));
            }
        } catch (SQLException e) {
            Logger.getLogger(JframeInfracciones.class.getName()).log(Level.SEVERE, null, e);
        }
    }

    public void addTable() {
        Statement s;
        try {
            s = (Statement) bD.conexion().createStatement();
            ResultSet rs = s.executeQuery("SELECT * FROM infracciones");
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            Object rowData[] = new Object[2];
            while (rs.next()) {
                rowData[0] = rs.getString("COD_INFRACCION");
                rowData[1] = rs.getString("DESCRIPCION_INFRACCION");
                model.addRow(rowData);
            }
        } catch (SQLException e) {
            Logger.getLogger(JframeInfracciones.class.getName()).log(Level.SEVERE, null, e);
        }
    }

