    public Integer getInfraccionValor(String nombre) throws SQLException{
        Statement stat = (Statement) conn.conexion().createStatement();
        ResultSet rs = stat.executeQuery("SELECT COD_INFRACCION FROM infracciones WHERE DESCRIPCION_INFRACCION = '"+nombre+"'");
        while(rs.next()){
            return rs.getInt("COD_INFRACCION");
        }
        return null;
    }
