    private void MostrarTablaMultasPersona(javax.swing.JTable TablaMultaPersona,javax.swing.JTextField ConsultaMultaCodUsu)
    {
        
        DefaultTableModel modelo = new DefaultTableModel()
        {
            @Override
            public boolean isCellEditable(int filas, int columnas) {
               if(columnas==7){
                   return true;
               }
               else
               {
                   return false;
               }
            }
          
        };
        modelo.addColumn("Codigo Multa");
        modelo.addColumn("Fecha");
        modelo.addColumn("Agente");
        modelo.addColumn("Articulo");
        modelo.addColumn("Lugar");
        modelo.addColumn("Importe");
        modelo.addColumn("Infraccion");
        

        TablaMultaPersona.setModel(modelo);
        Statement stat;
        String datos[] = new String[7];
        try {
             stat = (Statement) conn.conexion().createStatement();
             ResultSet rs = stat.executeQuery("SELECT * FROM MULTAS WHERE COD_PROPIETARIO = '"+ConsultaMultaCodUsu.getText()+"'");
             while(rs.next())
             {
                 datos[0] = rs.getString("cod_multa"); 
                 datos[1] = rs.getString("fecha_multa");
                 datos[2] = rs.getString("cod_agente");
                 datos[3] = rs.getString("articulo_multa");
                 datos[4] = rs.getString("lugar_multa");
                 datos[5] = rs.getString("importe_multa");
                 datos[6] = rs.getString("cod_infraccion");
                 modelo.addRow(datos);
             }
             TablaMultaPersona.setModel(modelo);
             
        } catch (SQLException ex) {
            Logger.getLogger(JFrameMultas.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        
    }
